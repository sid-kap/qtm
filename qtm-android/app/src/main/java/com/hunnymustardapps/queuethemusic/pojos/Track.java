package com.hunnymustardapps.queuethemusic.pojos;

public class Track {

    private String _title;
    private String _id;

    public Track() {}

    public void setTitle(String title) {
        _title = title;
    }

    public String getTitle() {
        return _title;
    }

    public void setId(String id) {
        _id = id;
    }

    public String getId() {
        return _id;
    }
}
